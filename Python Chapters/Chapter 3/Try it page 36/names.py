# Create a list of names
names = ["edith", "nchang", "aaron", "josephine", "ncha", "anucha", "fru", "khan", "desmond"]

# Loop through each name in the list
for name in names:
    # Create a message containing the current name followed by a period
    # .title() capitalizes the first letter of each word
    message = f"{name.title()}."
    
    # Print the message to the screen
    print(message)
