# Create a list of multiples of three from 3 up to 27
# range(start, stop, step) generates numbers:
# - start: 3 (the first number in the sequence)
# - stop: 30 (the sequence stops before this number)
# - step: 3 (increments by 3, which gives multiples of 3)
multiples_of_three = list(range(3, 30, 3))

# Print the list of multiples of three
print(multiples_of_three)