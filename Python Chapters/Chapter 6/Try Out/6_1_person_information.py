# Create a dictionary to store information about a person
# Dictionaries store data as key-value pairs: key is the label, value is the data
person = {
    'first_name': 'Khan',      # Key: 'first_name', Value: 'Khan'
    'last_name': 'Anucha',     # Key: 'last_name', Value: 'Anucha'
    'age': 13,                 # Key: 'age', Value: 13
    'city': 'Johannesburg'     # Key: 'city', Value: 'Johannesburg'
}

# Print each piece of information from the dictionary
# Access values using their corresponding keys
print("First name:", person["first_name"])  # Prints the first name
print("Last name:", person['last_name'])    # Prints the last name
print("Age:", person['age'])                # Prints the age
print("City:", person['city'])              # Prints the city